# ต่างกับ List ที่ไม่สามารถเปลี่ยนค่า,ลบค่า ได้ แต่สามารถเพิ่มค่าได้
# ใช้แค่ () ธรรมดา , List ใช้ []

number = (1, 2, 3, 4, 5)
mixed = (10, 20, [5, 4, 3], True, 'Max')

print(number[2])
print(mixed[1])
print(mixed[2])
# ไปที่ 2 แล้วเข้าไปที่อันดับที่ 1
print(mixed[2][1])

# ลองเปลี่ยนค่า มันจะเปลี่ยนไม่ได้
number[2] = 10
print(number)
